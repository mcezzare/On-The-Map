/// Struct to keep information from user of Udacity

struct UdacityUser: Codable {
    let udacityUserGuard, emailPreferences: EmailPreferences
    let cohortKeys: [JSONAny]
    let nickname: String
    let memberships: [JSONAny]
    let registered: Bool
    let affiliateProfiles, tags: [JSONAny]
    let hasPassword, employerSharing: Bool
    let email: Email
    let firstName, key, lastName: String
    let enrollments, principals: [JSONAny]
    let imageURL: String
    let badges, externalAccounts, socialAccounts: [JSONAny]
    
    enum CodingKeys: String, CodingKey {
        case udacityUserGuard = "guard"
        case emailPreferences = "email_preferences"
        case cohortKeys = "_cohort_keys"
        case nickname
        case memberships = "_memberships"
        case registered = "_registered"
        case affiliateProfiles = "_affiliate_profiles"
        case tags
        case hasPassword = "_has_password"
        case employerSharing = "employer_sharing"
        case email
        case firstName = "first_name"
        case key
        case lastName = "last_name"
        case enrollments = "_enrollments"
        case principals = "_principals"
        case imageURL = "_image_url"
        case badges = "_badges"
        case externalAccounts = "external_accounts"
        case socialAccounts = "social_accounts"
    }
}

